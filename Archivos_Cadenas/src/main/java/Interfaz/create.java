    void addItem(){
        jComboBox1.removeAllItems();    
        String[] items = Directory.getDirectoriesAsArrayString();
        for( int i = 0 ; i < items.length ; i++ ){
           jComboBox1.addItem(items[i]);
       }
       set = true;
    }
    boolean validateFields() {
        return !jTextField1.getText().isEmpty() &&
                !jTextArea1.getText().isEmpty();
    }
    void setDataInLabels(String text) {
        jLabel4.setText(
                "Palabras: " + Reader.wordCounter(text)
        );
        
        jLabel5.setText(
                "Caracteres: " + Reader.characterCount(text)
        );
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if ( validateFields() ){
            Writer.guardarText(
                    jTextField1.getText(),
                    jTextArea1.getText()
            );
            jTextField1.setText("");
            jTextArea1.setText("");
            setDataInLabels(jTextArea1.getText());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String readedText = Reader.readMyFile(jTextField1.getText());
        jTextArea1.setText(readedText);
        jTextField1.setText("");
        setDataInLabels(readedText);
    }//GEN-LAST:event_jButton2ActionPerformed

